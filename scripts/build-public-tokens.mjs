// Generates public/eq-tokens.css from the canonical @eq-solutions/tokens
// package, so the file published at core.eq.solutions/eq-tokens.css can never
// drift from the design system again. Wired into `pnpm run build`, so every
// production build re-emits it from the package.
//
// The published file keeps its own bespoke variable names (--eq-content-bg,
// --eq-sidebar-*, etc.) for backward compatibility; this script maps each one
// to a canonical token value. A few names are genuinely shell-only (the dark
// sidebar, the mono font) and have no package equivalent — those stay as
// literals in SHELL_ONLY below.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);

// Parse `--name: value;` pairs out of the package's generated tokens.css.
function loadCanonical() {
  const cssPath = require.resolve('@eq-solutions/tokens/tokens.css');
  const css = readFileSync(cssPath, 'utf8');
  const map = {};
  for (const m of css.matchAll(/(--eq-[a-z0-9-]+)\s*:\s*([^;]+);/gi)) {
    if (!(m[1] in map)) map[m[1]] = m[2].trim();
  }
  return map;
}

function hexToRgb(hex) {
  const h = hex.replace('#', '').trim();
  const n = parseInt(h.length === 3 ? h.split('').map((c) => c + c).join('') : h, 16);
  return `${(n >> 16) & 255}, ${(n >> 8) & 255}, ${n & 255}`;
}

const C = loadCanonical();

// Fail loudly if the package shape changed, rather than emit a broken file.
function need(name) {
  const v = C[name];
  if (!v) throw new Error(`build-public-tokens: missing ${name} in @eq-solutions/tokens — generator needs updating`);
  return v;
}

// Variables with no canonical equivalent: the dark shell sidebar and the mono
// font stack. Kept here as the single hand-maintained block.
const SHELL_ONLY = {
  '--eq-sidebar-bg': '#0F1117',
  '--eq-sidebar-border': '#1E2130',
  '--eq-sidebar-text': '#A8B3CF',
  '--eq-sidebar-text-active': '#FFFFFF',
  '--eq-sidebar-item-hover': '#1A1E2A',
  '--eq-font-mono': `'JetBrains Mono', 'Fira Code', monospace`,
};

const out = `/* GENERATED by scripts/build-public-tokens.mjs from @eq-solutions/tokens.
   Do not edit by hand — values are sourced from the design-token package so
   this file cannot drift. Published by eq-shell at /eq-tokens.css.
   Consume via: @import url('https://core.eq.solutions/eq-tokens.css') */

:root {
  /* Brand colours */
  --eq-sky:    ${need('--eq-sky')};
  --eq-deep:   ${need('--eq-deep')};
  --eq-ice:    ${need('--eq-ice')};
  --eq-ink:    ${need('--eq-ink')};
  --eq-sky-rgb: ${hexToRgb(need('--eq-sky'))};   /* RGB channels of --eq-sky, for rgba() tints */

  /* Accent — Direction D clay (brand decoration only, never status) */
  --eq-clay:      ${need('--eq-clay')};
  --eq-clay-deep: ${need('--eq-clay-deep')};
  --eq-clay-bg:   ${need('--eq-clay-bg')};

  /* Sidebar tokens (shell dark sidebar — shell-only, no package equivalent) */
  --eq-sidebar-bg:        ${SHELL_ONLY['--eq-sidebar-bg']};
  --eq-sidebar-border:    ${SHELL_ONLY['--eq-sidebar-border']};
  --eq-sidebar-text:      ${SHELL_ONLY['--eq-sidebar-text']};
  --eq-sidebar-text-active: ${SHELL_ONLY['--eq-sidebar-text-active']};
  --eq-sidebar-item-hover: ${SHELL_ONLY['--eq-sidebar-item-hover']};

  /* Content area */
  --eq-content-bg:   ${need('--eq-gray-50')};
  --eq-surface:      ${need('--eq-white')};
  --eq-border:       ${need('--eq-gray-200')};
  --eq-border-light: ${need('--eq-gray-100')};

  /* Text */
  --eq-text:        ${need('--eq-ink')};
  --eq-text-muted:  ${need('--eq-gray-500')};
  --eq-text-faint:  ${need('--eq-gray-400')};

  /* Status */
  --eq-success:  ${need('--eq-success-text')};
  --eq-warning:  ${need('--eq-warning-text')};
  --eq-danger:   ${need('--eq-error-text')};
  --eq-info:     ${need('--eq-sky')};

  /* Radius */
  --eq-radius-sm: ${need('--eq-radius-chip')};
  --eq-radius:    ${need('--eq-radius-card')};
  --eq-radius-lg: ${need('--eq-radius-shell')};

  /* Spacing scale (use multiples of 4) */
  --eq-space-1:  ${need('--eq-space-1')};
  --eq-space-2:  ${need('--eq-space-2')};
  --eq-space-3:  ${need('--eq-space-3')};
  --eq-space-4:  ${need('--eq-space-4')};
  --eq-space-6:  ${need('--eq-space-6')};
  --eq-space-8:  ${need('--eq-space-8')};

  /* Typography */
  --eq-font:       ${need('--eq-font-stack')};
  --eq-font-mono:  ${SHELL_ONLY['--eq-font-mono']};
  --eq-text-xs:   ${need('--eq-text-xs')};
  --eq-text-sm:   ${need('--eq-text-sm')};
  --eq-text-base: ${need('--eq-text-base')};
  --eq-text-lg:   ${need('--eq-text-lg')};
  --eq-text-xl:   ${need('--eq-text-xl')};
  --eq-text-2xl:  ${need('--eq-text-2xl')};
}
`;

const outPath = fileURLToPath(new URL('../public/eq-tokens.css', import.meta.url));
writeFileSync(outPath, out);
console.log(`build-public-tokens: wrote ${outPath}`);
